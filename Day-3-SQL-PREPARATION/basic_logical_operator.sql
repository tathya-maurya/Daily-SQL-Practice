--Question-11
--WRITE A QUERY TO DISPLAY ENAME AND DEPTNO, JOB OF EMPLOYEES WORKING AS CLERK IN DEPTNO 10 OR 20.
SELECT ENAME, DEPTNO, JOB
FROM EMP
WHERE JOB='CLERK' AND (DEPTNO=10 OR DEPTNO=20);


--Question-12
--WRITE A QUERY TO DISPLAY DETAILS OF EMPLOYEES WORKING AS CLERK OR MANAGER IN DEPTNO 10.
SELECT *
FROM EMP
WHERE  (JOB='CLERK' OR JOB='MANAGER') AND DEPTNO=10;


--Question-13
--WRITE A QUERY TO DISPLAY NAMES OF EMPLOYEES WORKING IN DEPTNO 10,20,30,40.
SELECT ENAME
FROM EMP
WHERE DEPTNO=10 OR DEPTNO=20 OR DEPTNO=30 OR DEPTNO=40;


--Question-14
--WRITE A QUERY TO DISPLAY DETAILS OF EMPLOYEES WITH EMPNO 7902, 7839.
