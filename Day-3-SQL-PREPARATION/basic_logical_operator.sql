--DAY-03 : Basic Query Using Logical Operator

--Question-1
--WRITE A QUERY TO DISPLAY OF THE EMPLOYEES WORKING AS CLERK AND EARNING LESS THAN 1500.
SELECT ENAME
FROM EMP
WHERE JOB='CLERK' AND SAL<1500;


--Question-2
--WRITE A QUERY TO DISPLAY ENAME AND HIREDATE OF THE EMPLOYEES WORKING AS MANAGER IN DEPARTMENT 30.
SELECT ENAME, HIREDATE
FROM EMP
WHERE JOB='MANAGER' AND DEPTNO=30;


--Question-3
--WRITE A QUERY TO DISPLAY DETAILS OF THE EMPLOYEE ALONG WITH ANNUAL SALARY IF THEY ARE WORKING IN DEPARTMENT 30 AS SALESMAN AND THEIR ANNUAL SALARY IS GREATER THAN 1400.
SELECT EMP.*, SAL*12 AS "ANNUAL SALARY"
FROM EMP
WHERE DEPTNO=30 AND JOB='SALESMAN' AND SAL*12>1400;


--Question-4
--WRITE A QUERY TO DISPLAY ALL THE DETAILS OF THE EMPLOYEE WORKING IN DEPARTMENT 30 OR AS ANALYST.
SELECT *
FROM EMP
WHERE DEPTNO=30 OR JOB='ANALYST';


--Question-5
--WRITE A QUERY TO DISPLAY NAMES OF THE EMPLOYEES WHOS SALARY IS LESS THAN 1100 AND THEIR, DESIGNATION IS CLERK.
SELECT ENAME
FROM EMP
WHERE SAL<1100 AND JOB='CLERK';


--Question-6
--WRITE A QUERY TO DISPLAY ENAME ,SAL, ANNUAL SALARY AND DEPTNO IF DEPTNO IS 20 AND EARNING MORE THAN 1100.
SELECT ENAME, SAL, DEPTNO, SAL*12 AS "ANNUAL SALARY"
FROM EMP
WHERE DEPTNO=20 AND SAL>1100;


--Question-7
--WRITE A QUERY TO DISPLAY EMPNO AND NAMES OF THE EMPLOYEES WORKING AS MANAGER IN DEPTNO 20.
SELECT EMPNO, ENAME
FROM EMP
WHERE JOB='MANAGER' AND DEPTNO=20;


--Question-8
--WRITE A QUERY TO DISPLAY DETAILS OF EMPLOYEES WORKING IN DEPTNO 20 OR 30.
