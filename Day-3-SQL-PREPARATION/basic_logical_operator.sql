--Question-16
--WRITE A QUERY TO DISPLAY NAMES OF EMPLOYEES HIRED AFTER 81 AND BEFORE 87.
SELECT ENAME
FROM EMP
WHERE HIREDATE>'31-DEC-81' AND HIREDATE<'01-JAN-87';


--Question-17
--WRITE A QUERY TO DISPLAY DETAILS OF EMPLOYEES EARNING MORE THAN 1250 BUT LESS THAN 3000.
SELECT *
FROM EMP
WHERE SAL>1250 AND SAL<3000;


--Question-18
--WRITE A QUERY TO DISPLAY NAMES OF EMPLOYEES HIRED AFTER 81 INTO DEPT 10 OR 30.
SELECT ENAME
FROM EMP
WHERE HIREDATE>'31-DEC-81' AND (DEPTNO=10 OR DEPTNO=30);

--Question-19
--WRITE A QUERY TO DISPLAY NAMES OF EMPLOYEES ALONG WITH ANNUAL SALARY FOR THE EMPLOYEES WORKING AS MANAGER OR CLERK INTO DEPT 10 OR 30.
SELECT ENAME, SAL*12 AS "ANNUAL SALARY"
FROM EMP
WHERE (JOB='MANAGER' OR JOB='CLERK') AND (DEPTNO=10 OR DEPTNO=30);

--Question-20
--WRITE A QUERY TO DISPLAY ALL THE DETAILS ALONG WITH ANNUAL SALARY IF SAL IS BETWEEN 1000 AND 4000 AND ANNUAL SALARY IS MORE THAN 15000.
SELECT EMP.*, SAL*12 AS "ANNUAL SALARY"
FROM EMP
WHERE SAL>=1000 AND SAL<=4000 AND SAL*12>15000;













 




